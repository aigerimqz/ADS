#include <bits/stdc++.h>
  
using namespace std;

struct Node{
	Node * prev;
	Node * next;
	string val;
	Node(string _val){
		prev = NULL;
		next = NULL;
		val = _val;
	}	
};

Node * head = NULL;
Node * tail = NULL;
int cnt;
void add_back(string s){
    Node* newNode = new Node(s);
    if (head == nullptr) {
        head = newNode;
        tail = newNode;
    } else {
        tail->next = newNode;
        newNode->prev = tail;
        tail = newNode;
    }
}
void add_front(string s){
    Node* newNode = new Node(s);
    if (head == nullptr) {
        head = newNode;
        tail = newNode;
    } else {
        newNode->next = head;
        head->prev = newNode;
        head = newNode;
    }
}
bool empty(){
    if(head == nullptr){
        return true;
    }else{
        return false;
    }

}
void erase_front(){
    if(head == nullptr){
        return; 
    }
    Node* curr = head;
    head = head->next;
    if(head != nullptr){
        head->prev = nullptr;
    }else{
        tail = nullptr;
    }

    delete curr;

}
void erase_back(){
    if(tail == nullptr){
        return;
    }
    Node* curr = tail;
    tail = tail->prev;
    if(tail != nullptr){
        tail->next = nullptr;
    }else{
        head = nullptr;
    }

    delete curr;
   
}
string front(){
    return head->val;
}
string back(){
    return tail->val;
}
void clear(){
    while(!empty()){
        erase_front();
    }
}
  
